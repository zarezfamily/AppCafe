// ─── FUNCIONES AUXILIARES ─────────────────────────────────────────────────────

const toFirestoreValue = (val) => {
  if (val === null || val === undefined) return { nullValue: null };
  if (typeof val === 'string') return { stringValue: val };
  if (typeof val === 'number') return { integerValue: String(val) };
  if (typeof val === 'boolean') return { booleanValue: val };
  return { stringValue: String(val) };
};

const toFields = (obj) => {
  const fields = {};
  for (const [key, val] of Object.entries(obj)) {
    fields[key] = toFirestoreValue(val);
  }
  return { fields };
};

const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

async function addCafe(cafe, index) {
  const foto = FOTOS[index % FOTOS.length];
  const data = {
    ...cafe,
    foto,
    uid: 'seed',
    fecha: new Date(Date.now() - index * 3600000).toISOString(),
  };

  const url = `${BASE_URL}/cafes?key=${FIREBASE_API_KEY}`;
  const res = await fetch(url, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(toFields(data)),
  });

  if (!res.ok) {
    const err = await res.text();
    throw new Error(`${res.status}: ${err}`);
  }
  return await res.json();
}

// ─── MAIN ─────────────────────────────────────────────────────────────────────

async function main() {
  if (!FIREBASE_PROJECT_ID || !FIREBASE_API_KEY) {
    console.log('\n⚠️  Faltan FIREBASE_PROJECT_ID y/o FIREBASE_API_KEY.\n');
    console.log(
      'Ejemplo: FIREBASE_PROJECT_ID=miappdecafe FIREBASE_API_KEY=AIza... node seed_cafes_v2.js\n'
    );
    return;
  }
  console.log(`\n☕ Seeding ${cafes.length} cafés con info completa...\n`);
  let ok = 0,
    fail = 0;

  for (let i = 0; i < cafes.length; i++) {
    const cafe = cafes[i];
    try {
      await addCafe(cafe, i);
      ok++;
      process.stdout.write(`\r✅ ${ok}/${cafes.length} — ${cafe.nombre.substring(0, 45)}`);
      await sleep(200);
    } catch (e) {
      fail++;
      console.error(`\n❌ Error en "${cafe.nombre}": ${e.message}`);
    }
  }

  console.log(`\n\n🎉 Completado: ${ok} cafés, ${fail} errores.\n`);
}

main();
